#define __STATIC_ASSERT(COND,MSG) 		typedef char static_assertion_##MSG[(!!(COND))*2-1]
#define __COMPILE_TIME_ASSERT3(X,L) 	__STATIC_ASSERT(X,static_assertion_at_line_##L)
#define __COMPILE_TIME_ASSERT2(X,L) 	__COMPILE_TIME_ASSERT3(X,L)

/// compile time assert
#define ASSERT_S(X)    					__COMPILE_TIME_ASSERT2(X,__LINE__)

//-----------------------------------------------------------------------------

/// runtime assert
#define ASSERT(X)    					assert(X)

